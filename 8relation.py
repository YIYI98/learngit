print(3<5>2)#3>5且5>2
print(3==3<5)#3==3且3《5
print('12345'>'23456')#第一个字符1<2
print('abcd'>'Abcd')#第一个字符'a'<'A'
print([85,92,73,84]<[91,82,73])#第一个数字85<91
print([1,2,300]>[1,2,3])#前两个数字相等，第三个数字300>3
print({1,2,3,4}>{3,4,5})#第一个集合不是第二个集合的超集
print({1,2,3,4}<={3,4,5})#第一个集合不是第二个集合的子集
print([1,2,3,4]>[1,2,3])#前三个元素相等，且第一个列表有多余的元素
